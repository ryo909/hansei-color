import { RelatedTypeGroup, TypeContent, TypeId, TypeMetrics } from './types';

type TextBlock = string | string[];

interface FinalContentDraft {
  catchCopy: string;
  intro: TextBlock;
  overview: TextBlock;
  readingPoints: string[];
  firstVoiceSamples: string[];
  opening: TextBlock;
  innerSubtitles: string[];
  innerProcess: TextBlock;
  observedBehaviors: string[];
  behaviorTraits: TextBlock;
  othersView: {
    friend: TextBlock;
    boss: TextBlock;
    stranger: TextBlock;
  };
  meetingPresence: string[];
  misunderstandingFromSelf?: TextBlock;
  misunderstandingFromOthers?: TextBlock;
  misunderstandingPoint?: TextBlock;
  reasonForGap: TextBlock;
  summary: TextBlock;
  relatedTypeGroups: RelatedTypeGroup[];
}

function toText(value: TextBlock | undefined) {
  if (!value) {
    return '';
  }

  return Array.isArray(value) ? value.join('\n\n') : value;
}

function flattenRelatedTypes(groups: RelatedTypeGroup[]) {
  return Array.from(new Set(groups.flatMap((group) => group.items)));
}

function buildTypeContent(draft: FinalContentDraft): TypeContent {
  return {
    catchCopy: draft.catchCopy,
    intro: toText(draft.intro),
    overview: toText(draft.overview),
    readingPoints: draft.readingPoints,
    firstVoiceSamples: draft.firstVoiceSamples,
    opening: toText(draft.opening),
    innerSubtitles: draft.innerSubtitles,
    innerProcess: toText(draft.innerProcess),
    observedBehaviors: draft.observedBehaviors,
    behaviorTraits: toText(draft.behaviorTraits),
    othersView: {
      friend: toText(draft.othersView.friend),
      boss: toText(draft.othersView.boss),
      stranger: toText(draft.othersView.stranger),
    },
    meetingPresence: draft.meetingPresence,
    misunderstandingFromSelf: toText(draft.misunderstandingFromSelf),
    misunderstandingFromOthers: toText(draft.misunderstandingFromOthers),
    misunderstandingPoint: toText(draft.misunderstandingPoint),
    reasonForGap: toText(draft.reasonForGap),
    summary: toText(draft.summary),
    relatedTypeGroups: draft.relatedTypeGroups,
    relatedTypes: flattenRelatedTypes(draft.relatedTypeGroups),
  };
}

